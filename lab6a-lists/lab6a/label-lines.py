# s/i = seq of lines, terminated in "end"

# s/o = 1. the positition of the line
#       2. the total no of lines
#       3. the element at that pos

# gotta read oll the lines first

a = []

n = input()
while n != "end":
   a. append(n)
   n = input()

if len(a) != 0:
   i = 0
   while i < len(a):
      print(i, len(a), a[i])
      i += 1
